# NotImplementedError는 파이썬 내장 오류로, 꼭 작성해야 하는 부분이 구현되지 않았을 경우 일부러 오류를 발생시키고자 사용한다.
class Bird:
    def fly(self):
        raise NotImplementedError

class Eagle(Bird):
    # pass
    def fly(self):
        print("날아올라~")

egle = Eagle()
egle.fly()




# 오류 만들기
class MyError(Exception):
    def __str__(self):
        return "허용되지 않는 별명입니다."

def say_nick(nick):
    if nick == '바보':
        raise MyError()
    print(nick)

try:
    say_nick('천사')
    say_nick('바보')
except MyError as e:
    print(e)

